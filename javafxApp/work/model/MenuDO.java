package model;

public class MenuDO {

	private String type;
	private String name;
	
	
	
	
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getType() {
		return type;
	}
	public void setType(String type) {
		this.type = type;
	}
	
	@Override
	public String toString() {
		return "MenuController [name=" + name + ", type=" + type + "]";
	}
	
	
}

